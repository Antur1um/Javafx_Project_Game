package com.example.game.Unit;

public class Unit {

    private int Salary;
    private int Power;
    private int Cost;


    public int getSalary() {
        return Salary;
    }

    public int getPower() {
        return Power;
    }

    public int getCost() {
        return Cost;
    }

    public void setCost(int cost) {
        Cost = cost;
    }

    public void setPower(int power) {
        Power = power;
    }

    public void setSalary(int salary) {
        Salary = salary;
    }
}